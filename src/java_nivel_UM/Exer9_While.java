package java_nivel_UM;

public class Exer9_While {

	public static void main(String[] args) {

		int incremento = 1;
		System.out.println("Iniciando o Sistem");

		while (incremento <= 10) {
			System.out.println("--> Executando o contador");
			incremento++;
		}
		System.out.println("Terminando o Programa");
	}

}
